/* WEリーグ */
function WELeague() {
	$("h2").css("background-color", "white").css("color", "black");
	$("#JLeague div").hide();
	$("#input_Location").hide();
	$("#Route").hide();
	$(".Selection" || ".Selected").hide();
	$("#NPB div").hide();
	$("#WE").show();
}

function WE_show() {
	$("h2").css("background-color", "white").css("color", "black");
	$("#JLeague div").hide();
	$("#input_Location").hide();
	$("#Route").hide();
	$(".Selection" || ".Selected").hide();
	$("#NPB div").hide();
	$("#WE").show();
}

